package org.openejb.ri.server;


public class AuthenticationRequest implements java.security.Principal, java.io.Serializable {

    private String userID;
    private String password;

    public AuthenticationRequest(String userID, String password) {
        this.userID = userID;
        this.password = password;
    }
    public String getUserID( ) {
        return userID;
    }
    public String getPassword( ) {
        return password;
    }

    public boolean equals(Object another) {
        if ( another instanceof AuthenticationRequest ) {
            AuthenticationRequest other = (AuthenticationRequest)another;
            if ( other.userID == userID && other.password == password )
                return true;
        }
        return false;
    }
    public String getName() {
        return getUserID();
    }
    public int hashCode() {
        return toString().hashCode();
    }
    public String toString() {
        return(userID+":"+password);
    }
}
